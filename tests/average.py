# Function to calculate averages and log them
def calculate_and_log_averages(log_filepath):
    with open(log_filepath, "w") as log_file:
        arrays = {
            "Fusion IoU": fusion_iou,
            "Fusion F1": fusion_f1,
            "Fusion Precision": fusion_precision,
            "Fusion Recall": fusion_recall,
            "Fusion Training Time": fusion_training_time,
            "Fusion Inference Time": fusion_inference_time,
            "RGB IoU": rgb_iou,
            "RGB F1": rgb_f1,
            "RGB Precision": rgb_precision,
            "RGB Recall": rgb_recall,
            "RGB Training Time": rgb_training_time,
            "RGB Inference Time": rgb_inference_time,
            "RGB+IR IoU": rgb_ir_iou,
            "RGB+IR F1": rgb_ir_f1,
            "RGB+IR Precision": rgb_ir_precision,
            "RGB+IR Recall": rgb_ir_recall,
            "RGB+IR Training Time": rgb_ir_training_time,
            "RGB+IR Inference Time": rgb_ir_inference_time,
        }

        # Calculate averages
        for key, value in arrays.items():
            avg_value = np.mean(value)
            print(f"{key} Average: {avg_value:.4f}")
            log_file.write(f"{key} Average: {avg_value:.4f}\n")


# Define log file path
